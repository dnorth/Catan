package client.models.mapdata;


/**
 * Holds x and y coordinates of hex
 *
 */

public class HexLocation {
	private int xcoord;
	private int ycoord;
	
	public int getXcoord() {
		return xcoord;
	}
	public void setXcoord(int xcoord) {
		this.xcoord = xcoord;
	}
	public int getYcoord() {
		return ycoord;
	}
	public void setYcoord(int ycoord) {
		this.ycoord = ycoord;
	}
}